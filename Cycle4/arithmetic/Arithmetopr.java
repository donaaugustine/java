package arithmetic;
public class Arithmetopr
{
        private int a,b;
        public Arithmetopr(int c,int d)
                {
                                a=c;
                                b=d;
                }
        public double add()
                {
                                return (a+b);
                }
        public double div()
                {
                                return (a/b);
                }
        public double sub()
                {
                                return (a-b);
                }
        public double mul()
                {
                                return (a*b);
                }
}